#include "drivers\stm32f4_system.h"
#include "modules\otm8009.h"
#include "modules\ov7725.h"

#include "qCopterMV.h"
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Init( void )
{
  HAL_InitTick();

  QMV_GPIO_Config();
  QMV_LCD_Config();
  QMV_Camera_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Loop( void )
{
  LCD_SetWindow(0, 0, FRAME_W - 1, FRAME_H - 1);
  OV7725_Start();

  while(1) {
    LED_B_Toggle();
    delay_ms(500);
  }
}
