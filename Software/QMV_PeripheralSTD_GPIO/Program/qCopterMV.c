#include "drivers\stm32f4_system.h"

#include "qCopterMV.h"
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Init( void )
{
  HAL_InitTick();

  QMV_GPIO_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Loop( void )
{
  while(1) {
    LED_B_Set();
    LED_R_Reset();
    delay_ms(200);
    LED_R_Set();
    LED_G_Toggle();
    delay_ms(200);
    LED_G_Set();
    LED_B_Toggle();
    delay_ms(200);
    while(KEY_Read()) {
      LED_R_Set();
      LED_G_Set();
      LED_B_Set();
      delay_ms(200);
      LED_R_Reset();
      LED_G_Reset();
      LED_B_Reset();
      delay_ms(200);
    }
  }
}
