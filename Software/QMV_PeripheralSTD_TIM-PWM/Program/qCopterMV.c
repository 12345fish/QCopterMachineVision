#include "drivers\stm32f4_system.h"
#include "drivers\stm32f4_tim_pwm.h"

#include "qCopterMV.h"
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Init( void )
{
  HAL_InitTick();

  QMV_GPIO_Config();
  QMV_PWM_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Loop( void )
{
  uint8_t state = 0;
  uint32_t i = PWM_MIN;

  while(1) {
    i = (state) ? (i - 1) : (i + 1);
    if(i == PWM_MAX){ state = 1; }
    if(i == PWM_MIN){ state = 0; }

    TIM_PWM_setDuty(i);

    LED_G_Toggle();
    delay_ms(1);
  }
}
