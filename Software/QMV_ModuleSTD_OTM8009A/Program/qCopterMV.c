#include "drivers\stm32f4_system.h"
#include "modules\otm8009.h"

#include "qCopterMV.h"
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Init( void )
{
  HAL_InitTick();

  QMV_GPIO_Config();
  QMV_LCD_Config();
}
/*====================================================================================================*/
/*====================================================================================================*/
void QMV_Loop( void )
{
  LCD_TestFunction();

  while(1) {
    LED_R_Toggle();
    LED_G_Toggle();
    LED_B_Toggle();
    delay_ms(100);
  }
}
